# --- ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• ---
def load_data():
    current_sheet = get_current_sheet_name()
    try:
        # ‡∏≠‡πà‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏à‡∏≤‡∏Å Sheet, ‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡∏°‡∏µ‡πÉ‡∏´‡πâ‡∏™‡∏£‡πâ‡∏≤‡∏á DataFrame ‡πÄ‡∏õ‡∏•‡πà‡∏≤
        df = conn.read(worksheet=current_sheet, usecols=['Date', '‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£', '‡∏£‡∏≤‡∏Ñ‡∏≤', '‡∏à‡∏≥‡∏ô‡∏ß‡∏ô/‡∏ä‡∏¥‡πâ‡∏ô', '‡∏£‡∏ß‡∏°/‡∏ö‡∏≤‡∏ó'])
        if df.empty:
            return pd.DataFrame(columns=['Date', '‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£', '‡∏£‡∏≤‡∏Ñ‡∏≤', '‡∏à‡∏≥‡∏ô‡∏ß‡∏ô/‡∏ä‡∏¥‡πâ‡∏ô', '‡∏£‡∏ß‡∏°/‡∏ö‡∏≤‡∏ó'])
        
        # ‡∏à‡∏±‡∏î Format ‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà
        df['Date'] = pd.to_datetime(df['Date']).dt.date
        return df
    except Exception:
        return pd.DataFrame(columns=['Date', '‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£', '‡∏£‡∏≤‡∏Ñ‡∏≤', '‡∏à‡∏≥‡∏ô‡∏ß‡∏ô/‡∏ä‡∏¥‡πâ‡∏ô', '‡∏£‡∏ß‡∏°/‡∏ö‡∏≤‡∏ó'])

# ‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÄ‡∏Ç‡πâ‡∏≤ Session State ‡∏Ñ‡∏£‡∏±‡πâ‡∏á‡πÅ‡∏£‡∏Å
if 'df' not in st.session_state:
    st.session_state.df = load_data()

# --- 2. ‡∏™‡πà‡∏ß‡∏ô‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• (‡∏õ‡∏£‡∏±‡∏ö‡∏õ‡∏£‡∏∏‡∏á‡πÉ‡∏´‡∏°‡πà ‡πÑ‡∏°‡πà‡πÉ‡∏ä‡πâ st.form ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏´‡πâ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì Real-time) ---
st.title("üç∞ ‡∏£‡∏∞‡∏ö‡∏ö‡πÄ‡∏ö‡∏¥‡∏Å‡∏Ç‡∏≠‡∏á/‡∏¢‡∏≠‡∏î‡∏Ç‡∏≤‡∏¢ ‡∏£‡πâ‡∏≤‡∏ô Nami")
st.subheader(f"üìÖ ‡∏õ‡∏£‡∏∞‡∏à‡∏≥‡πÄ‡∏î‡∏∑‡∏≠‡∏ô: {get_current_sheet_name()}")

# Layout ‡∏™‡πà‡∏ß‡∏ô‡∏Å‡∏£‡∏≠‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•
with st.container(border=True):
    st.markdown("### üìù ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡πÉ‡∏´‡∏°‡πà")
    
    col1, col2, col3, col4 = st.columns([2, 3, 2, 2])

    with col1:
        date_val = st.date_input("‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà", value=datetime.now().date(), key="input_date")

    with col2:
        # Dropdown ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£
        selected_item = st.selectbox(
            "‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤", 
            options=list(MENU_PRESETS.keys()), 
            index=0,
            key="selected_menu_item"
        )
        
        # ‡∏ñ‡πâ‡∏≤‡πÄ‡∏•‡∏∑‡∏≠‡∏Å "‡∏Å‡∏≥‡∏´‡∏ô‡∏î‡πÄ‡∏≠‡∏á" ‡πÉ‡∏´‡πâ‡∏û‡∏¥‡∏°‡∏û‡πå‡∏ä‡∏∑‡πà‡∏≠, ‡∏ñ‡πâ‡∏≤‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡∏≠‡∏∑‡πà‡∏ô ‡πÉ‡∏´‡πâ‡πÉ‡∏ä‡πâ‡∏ä‡∏∑‡πà‡∏≠‡∏ô‡∏±‡πâ‡∏ô
        if selected_item == "‡∏Å‡∏≥‡∏´‡∏ô‡∏î‡πÄ‡∏≠‡∏á (‡∏û‡∏¥‡∏°‡∏û‡πå‡∏ä‡∏∑‡πà‡∏≠‡πÉ‡∏´‡∏°‡πà)":
            item_name = st.text_input("‡∏£‡∏∞‡∏ö‡∏∏‡∏ä‡∏∑‡πà‡∏≠‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤", key="manual_item_name")
        else:
            item_name = selected_item
            # ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏£‡∏≤‡∏Ñ‡∏≤‡∏ï‡∏≤‡∏°‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡∏ó‡∏µ‡πà‡πÄ‡∏•‡∏∑‡∏≠‡∏Å (‡∏ñ‡πâ‡∏≤‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ‡πÅ‡∏Å‡πâ‡∏£‡∏≤‡∏Ñ‡∏≤)
            # ‡∏´‡∏°‡∏≤‡∏¢‡πÄ‡∏´‡∏ï‡∏∏: ‡πÉ‡∏ô Logic ‡∏á‡πà‡∏≤‡∏¢‡πÜ ‡∏ô‡∏µ‡πâ ‡∏£‡∏≤‡∏Ñ‡∏≤‡∏à‡∏∞ Default ‡∏ï‡∏≤‡∏°‡πÄ‡∏°‡∏ô‡∏π ‡πÅ‡∏ï‡πà‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç‡∏ó‡∏µ‡∏´‡∏•‡∏±‡∏á‡πÑ‡∏î‡πâ

    with col3:
        # ‡∏î‡∏∂‡∏á‡∏£‡∏≤‡∏Ñ‡∏≤ Default ‡∏°‡∏≤‡πÅ‡∏™‡∏î‡∏á
        default_price = MENU_PRESETS.get(selected_item, 0.0)
        # Input ‡∏£‡∏≤‡∏Ñ‡∏≤ (‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç‡πÑ‡∏î‡πâ)
        price_val = st.number_input("‡∏£‡∏≤‡∏Ñ‡∏≤‡∏ï‡πà‡∏≠‡∏ä‡∏¥‡πâ‡∏ô", value=float(default_price), min_value=0.0, step=1.0, key="input_price")

    with col4:
        qty_val = st.number_input("‡∏à‡∏≥‡∏ô‡∏ß‡∏ô (‡∏ä‡∏¥‡πâ‡∏ô)", min_value=1, step=1, value=1, key="input_qty")

    # ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏¢‡∏≠‡∏î‡∏£‡∏ß‡∏° Real-time ‡πÉ‡∏´‡πâ‡πÄ‡∏´‡πá‡∏ô‡∏Å‡πà‡∏≠‡∏ô‡∏Å‡∏î‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å
    total_val = price_val * qty_val
    st.info(f"üí∞ ‡∏¢‡∏≠‡∏î‡∏£‡∏ß‡∏°‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡∏ô‡∏µ‡πâ: **{total_val:,.2f} ‡∏ö‡∏≤‡∏ó**")

    # ‡∏õ‡∏∏‡πà‡∏°‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å
    if st.button("üíæ ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÅ‡∏•‡∏∞‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î", type="primary", use_container_width=True):
        if item_name == "":
            st.error("‚ùå ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏£‡∏∞‡∏ö‡∏∏‡∏ä‡∏∑‡πà‡∏≠‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤")
        else:
            # 1. ‡∏™‡∏£‡πâ‡∏≤‡∏á Data ‡πÅ‡∏ñ‡∏ß‡πÉ‡∏´‡∏°‡πà
            new_row = pd.DataFrame([{
                'Date': date_val,
                '‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£': item_name,
                '‡∏£‡∏≤‡∏Ñ‡∏≤': price_val,
                '‡∏à‡∏≥‡∏ô‡∏ß‡∏ô/‡∏ä‡∏¥‡πâ‡∏ô': qty_val,
                '‡∏£‡∏ß‡∏°/‡∏ö‡∏≤‡∏ó': total_val
            }])

            # 2. ‡∏£‡∏ß‡∏°‡∏Å‡∏±‡∏ö‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÄ‡∏î‡∏¥‡∏°‡πÉ‡∏ô Session State (Local View Update)
            st.session_state.df = pd.concat([st.session_state.df, new_row], ignore_index=True)

            # 3. ‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡∏∂‡πâ‡∏ô Google Sheets (Cloud Update)
            current_sheet = get_current_sheet_name()
            try:
                conn.update(worksheet=current_sheet, data=st.session_state.df)
                st.toast(f"‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å '{item_name}' ‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à!", icon="‚úÖ")
                
                # --- ‡πÄ‡∏ó‡∏Ñ‡∏ô‡∏¥‡∏Ñ‡∏Å‡∏≤‡∏£‡∏•‡πâ‡∏≤‡∏á‡∏Ñ‡πà‡∏≤ (Reset Form) ---
                # ‡πÄ‡∏ô‡∏∑‡πà‡∏≠‡∏á‡∏à‡∏≤‡∏Å‡πÄ‡∏£‡∏≤‡∏ñ‡∏≠‡∏î st.form ‡∏≠‡∏≠‡∏Å ‡πÄ‡∏£‡∏≤‡∏ï‡πâ‡∏≠‡∏á‡∏•‡πâ‡∏≤‡∏á‡∏Ñ‡πà‡∏≤‡∏î‡πâ‡∏ß‡∏¢‡∏Å‡∏≤‡∏£ Rerun ‡∏´‡∏£‡∏∑‡∏≠‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£ Key
                # ‡∏ß‡∏¥‡∏ò‡∏µ‡∏á‡πà‡∏≤‡∏¢‡∏™‡∏∏‡∏î‡∏Ñ‡∏∑‡∏≠‡∏Å‡∏≤‡∏£‡∏Å‡∏î‡πÅ‡∏•‡πâ‡∏ß‡πÉ‡∏´‡πâ‡∏°‡∏±‡∏ô‡∏£‡∏µ‡πÄ‡∏ü‡∏£‡∏ä‡∏´‡∏ô‡πâ‡∏≤ ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÄ‡∏Ñ‡∏•‡∏µ‡∏¢‡∏£‡πå‡∏Ñ‡πà‡∏≤ Input ‡∏Å‡∏•‡∏±‡∏ö‡πÄ‡∏õ‡πá‡∏ô Default
                # ‡∏´‡∏£‡∏∑‡∏≠‡∏ñ‡πâ‡∏≤‡∏ï‡πâ‡∏≠‡∏á‡∏Å‡∏≤‡∏£‡πÄ‡∏Ñ‡∏•‡∏µ‡∏¢‡∏£‡πå‡πÄ‡∏â‡∏û‡∏≤‡∏∞ field ‡∏ï‡πâ‡∏≠‡∏á‡πÉ‡∏ä‡πâ Session State callback (‡πÅ‡∏ï‡πà‡∏à‡∏∞‡∏ã‡∏±‡∏ö‡∏ã‡πâ‡∏≠‡∏ô‡∏Ç‡∏∂‡πâ‡∏ô)
                # ‡∏ï‡∏£‡∏á‡∏ô‡∏µ‡πâ‡∏ú‡∏°‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡πÉ‡∏ä‡πâ rerun ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ä‡∏±‡∏ß‡∏£‡πå‡∏Ç‡∏≠‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•
                st.rerun() 
                
            except Exception as e:
                st.error(f"‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å: {e}")

st.divider()

# --- 3. ‡∏™‡πà‡∏ß‡∏ô Dashboard ‡∏™‡∏£‡∏∏‡∏õ‡∏¢‡∏≠‡∏î ---
current_df = st.session_state.df

if not current_df.empty:
    st.subheader("üìä Dashboard ‡∏™‡∏£‡∏∏‡∏õ‡∏¢‡∏≠‡∏î‡∏Ç‡∏≤‡∏¢")
    
    # ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏¢‡∏≠‡∏î
    today = datetime.now().date()
    # ‡πÅ‡∏õ‡∏•‡∏á‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå Date ‡πÄ‡∏õ‡πá‡∏ô date object ‡πÉ‡∏´‡πâ‡∏ä‡∏±‡∏ß‡∏£‡πå‡∏Å‡πà‡∏≠‡∏ô‡πÄ‡∏ó‡∏µ‡∏¢‡∏ö
    current_df['Date'] = pd.to_datetime(current_df['Date']).dt.date
    
    daily_df = current_df[current_df['Date'] == today]
    
    daily_total = daily_df['‡∏£‡∏ß‡∏°/‡∏ö‡∏≤‡∏ó'].sum()
    daily_qty = daily_df['‡∏à‡∏≥‡∏ô‡∏ß‡∏ô/‡∏ä‡∏¥‡πâ‡∏ô'].sum()
    monthly_total = current_df['‡∏£‡∏ß‡∏°/‡∏ö‡∏≤‡∏ó'].sum()

    m1, m2, m3 = st.columns(3)
    m1.metric("‡∏¢‡∏≠‡∏î‡∏Ç‡∏≤‡∏¢‡∏ß‡∏±‡∏ô‡∏ô‡∏µ‡πâ", f"{daily_total:,.2f} ‡∏ö‡∏≤‡∏ó", delta=f"{daily_qty} ‡∏ä‡∏¥‡πâ‡∏ô")
    m2.metric("‡∏¢‡∏≠‡∏î‡∏Ç‡∏≤‡∏¢‡πÄ‡∏î‡∏∑‡∏≠‡∏ô‡∏ô‡∏µ‡πâ", f"{monthly_total:,.2f} ‡∏ö‡∏≤‡∏ó")
    m3.metric("‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡∏ß‡∏±‡∏ô‡∏ô‡∏µ‡πâ", f"{len(daily_df)} ‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£")

    st.divider()

    # --- 4. ‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÅ‡∏•‡∏∞‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç ---
    tab1, tab2 = st.tabs(["üìÖ ‡∏™‡∏£‡∏∏‡∏õ‡∏¢‡∏≠‡∏î‡πÅ‡∏¢‡∏Å‡∏ï‡∏≤‡∏°‡∏ß‡∏±‡∏ô", "üìù ‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î (‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç/‡∏•‡∏ö)"])

    with tab1:
        # Group by ‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà
        summary_by_date = current_df.groupby('Date')[['‡∏à‡∏≥‡∏ô‡∏ß‡∏ô/‡∏ä‡∏¥‡πâ‡∏ô', '‡∏£‡∏ß‡∏°/‡∏ö‡∏≤‡∏ó']].sum().sort_index(ascending=False)
        st.dataframe(summary_by_date, use_container_width=True)

    with tab2:
        st.caption("üí° ‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç‡πÉ‡∏ô‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡πÑ‡∏î‡πâ‡πÄ‡∏•‡∏¢ ‡πÅ‡∏•‡πâ‡∏ß‡∏Å‡∏î‡∏õ‡∏∏‡πà‡∏° '‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï' ‡∏î‡πâ‡∏≤‡∏ô‡∏•‡πà‡∏≤‡∏á")
        
        # Data Editor
        edited_all_df = st.data_editor(
            current_df, 
            num_rows="dynamic", 
            use_container_width=True,
            column_config={
                "‡∏£‡∏ß‡∏°/‡∏ö‡∏≤‡∏ó": st.column_config.NumberColumn(disabled=True) # ‡∏•‡πá‡∏≠‡∏Å‡∏ä‡πà‡∏≠‡∏á‡∏£‡∏ß‡∏°‡πÄ‡∏á‡∏¥‡∏ô ‡πÑ‡∏°‡πà‡πÉ‡∏´‡πâ‡πÅ‡∏Å‡πâ (‡πÉ‡∏´‡πâ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡πÄ‡∏≠‡∏á)
            },
            key="main_editor"
        )

        if st.button("üîÑ ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏Å‡∏≤‡∏£‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î‡∏Ç‡∏∂‡πâ‡∏ô Cloud"):
            # ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏ä‡πà‡∏≠‡∏á '‡∏£‡∏ß‡∏°/‡∏ö‡∏≤‡∏ó' ‡πÉ‡∏´‡∏°‡πà‡∏ó‡∏∏‡∏Å‡∏ö‡∏£‡∏£‡∏ó‡∏±‡∏î (‡πÅ‡∏Å‡πâ Bug ‡πÄ‡∏î‡∏¥‡∏°‡∏Ç‡∏≠‡∏á‡∏Ñ‡∏∏‡∏ì‡∏ï‡∏£‡∏á‡∏ô‡∏µ‡πâ)
            edited_all_df['‡∏£‡∏ß‡∏°/‡∏ö‡∏≤‡∏ó'] = edited_all_df['‡∏£‡∏≤‡∏Ñ‡∏≤'] * edited_all_df['‡∏à‡∏≥‡∏ô‡∏ß‡∏ô/‡∏ä‡∏¥‡πâ‡∏ô']
            
            # ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏Å‡∏•‡∏±‡∏ö‡πÄ‡∏Ç‡πâ‡∏≤ Session State
            st.session_state.df = edited_all_df
            
            # ‡∏™‡πà‡∏á‡∏Ç‡∏∂‡πâ‡∏ô Google Sheets
